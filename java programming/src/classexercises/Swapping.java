package classexercises;

public class Swapping {

	public static void main(String[] args) {
		
		int a=10;
		int b=50;
		System.out.print("Value of a before swapping "+ a);
		System.out.println();
	    System.out.print("Value of b before swapping "+ b);
	System.out.println();
	int temp=a;
	a=b;
	b=temp;
	
	System.out.print("After swapping ");
	System.out.println();
	System.out.print("Value of a after swapping "+ a);
	System.out.println();
    System.out.print("Value of b after swapping "+ b);
	
	}
}
